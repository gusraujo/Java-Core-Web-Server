package classes.ConcurrencyChallenges;

import java.util.Random;

public class MetricUseCase {
    public static void main(String[] args) {
        Metrics metric = new Metrics();
        BusinessLogic businessLogic1 = new BusinessLogic(metric);
        BusinessLogic businessLogic2 = new BusinessLogic(metric);
        BusinessLogic businessLogic3 = new BusinessLogic(metric);

        MetricsPrinter metricsPrinter = new MetricsPrinter(metric);
        businessLogic1.start();
        businessLogic2.start();
        businessLogic3.start();
        metricsPrinter.start();
    }

    public static class MetricsPrinter extends Thread {
        private Metrics metrics;

        public MetricsPrinter(Metrics metrics) {
            this.metrics = metrics;
        }

        @Override
        public void run() {
            while (true) {
                try {
                    Thread.sleep(1);
                } catch (InterruptedException e) {
                }

                double currentAverage = metrics.getAverage();

                System.out.println("Current Average is " + currentAverage);
            }
        }
    }

    public static class BusinessLogic extends Thread {
        private Metrics metrics;
        private Random random = new Random();

        public BusinessLogic(Metrics metrics) {
            this.metrics = metrics;
        }

        @Override
        public void run() {
            while (true) {
                long start = System.currentTimeMillis();

                try {
                    Thread.sleep(random.nextInt(2));
                } catch (InterruptedException e) {
                }

                long end = System.currentTimeMillis();

                metrics.addSample(end - start);
            }
        }
    }

    public static class Metrics {
        private long count = 0;
        private volatile double average = 0.0;

        public synchronized void addSample(long sample) {
            double currentSum = average * count;
            count++;
            average = (currentSum + sample) / count;
        }

        public double getAverage() {
            return average;
        }
    }
}
